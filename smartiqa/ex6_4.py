def del_from_tuple(t, elem) -> tuple[int | str, ...]:
    """
    Удаляет первое появление определенного элемента из кортежа по значению и возвращать кортеж без оного.
    Если данного элемента в кортеже нет, то кортеж вернется функцией в исходном виде
    >>> del_from_tuple((1, 2, 3, 2, 1), 2)
    (1, 3, 2, 1)
    >>> del_from_tuple((6, 1, 4.0, 7), 4.0)
    (6, 1, 7)
    >>> del_from_tuple((6, 1, 4.7, 7), 4.7)
    (6, 1, 7)
    >>> del_from_tuple((6, 1, 4, 7), 4)
    (6, 1, 7)
    >>> del_from_tuple((1, 2, 3), 5)  # элемента нет
    (1, 2, 3)
    >>> del_from_tuple(('a', 'b', 'c'), 'b')
    ('a', 'c')
    >>> del_from_tuple((1, 1, 1, 1), 1)
    (1, 1, 1)
    >>> del_from_tuple((5,), 5)  # единственный элемент
    ()
    >>> del_from_tuple((), 1)  # пустой кортеж
    ()
    """
    if t.count(elem) == 0:
        return t
    else:
        idx = t.index(elem)
        return t[:idx] + t[idx + 1:]
